# Tipos de Variáveis

# inteiros
valorX = 10
valorY = 8

valor = valorX + valorY 
valor = valorX * valorY
valor = valorX / valorY
valor = valorX - valorY
valor = valorX % valorY # módulo

# reais
valorX = 10.8
valorY = 8.5

valor = valorX + valorY 
valor = valorX * valorY
valor = valorX / valorY
valor = valorX - valorY
valor = valorX // valorY # divisão inteira

# booleanos
valor = True
valor = False

# textuais
palavraX = "Acerola"
palavraY = "PorVoce"

frase = palavraX + palavraY # concatenação
print(len(frase)) # tamanho da frase
print(frase.isdigit()) # verificação se existe apenas números nessa frase
print(frase.isalnum()) # verificação se existem números e letras nessa frase
print(frase.count("a")) # contagem das vezes em que o caracter aparece